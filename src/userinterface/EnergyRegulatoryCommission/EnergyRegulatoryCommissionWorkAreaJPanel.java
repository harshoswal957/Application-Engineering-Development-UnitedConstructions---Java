    private void genReportjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genReportjButtonActionPerformed
        //reports generated with the regulatory
        File file = new File("C:\\Users\\Harsh\\Documents\\NetBeansProjects\\UnitedConstructionProject\\src\\userinterface\\EnergyRegulatoryCommission\\ReportProcess.csv");
        float energySaving = 0;
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise instanceof UnitedConstructionEnterprise) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof ReportingOrganization) {

                            energySaving = ((ReportingOrganization) organization).getReport().getTotalEnergySaving();

                        }
                    }
                }
            }
        }

        String content = "The Energy Savings Reports is as follows:" + "    " +"The cumulative Energy Savings is:" + " " + energySaving + "%";

        try (FileOutputStream fop = new FileOutputStream(file)) {

            // Create new file if file dosen't exist
            if (!file.exists()) {
                file.createNewFile();
            }

            // Get file content in bytes
            byte[] contentInBytes = content.getBytes();

            fop.write(contentInBytes);
            fop.flush(); //write the content of the buffer to the file
            fop.close(); //closes the stream permanently

        } catch (IOException e) {
            e.printStackTrace(); //prints the throwable
        }

        JOptionPane.showMessageDialog(null, "Report fetched and saved sucesfully", "Info", JOptionPane.INFORMATION_MESSAGE);
        // TODO add your handling code here:
    }//GEN-LAST:event_genReportjButtonActionPerformed

