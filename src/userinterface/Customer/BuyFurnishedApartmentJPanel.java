   private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) RealtorApartmentsJTable.getModel();
        dtm.setRowCount(0);
        for (Organization realtorOrganization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (realtorOrganization instanceof RealtorOrganization) {
                ApartmentCatalog aptCatalogat = ((RealtorOrganization) realtorOrganization).getAptCatalog();

                for (RealtorApartment apt : aptCatalogat.getListOfRealtorApartments()) {
                    Object[] row = new Object[4];
                    row[0] = apt;
                    row[1] = apt.getApartmentType();
                    row[2] = apt.getHousingComplexType();
                    row[3] = apt.getPrice();
                    dtm.addRow(row);
                }
            }
        }
    }


