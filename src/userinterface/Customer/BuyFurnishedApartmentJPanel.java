    private void placeOrderJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_placeOrderJButtonActionPerformed
        int selectedRow = RealtorApartmentsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        RealtorApartment apt = (RealtorApartment) RealtorApartmentsJTable.getValueAt(selectedRow, 0);
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise instanceof CustomerSupportEnterprise) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof RealtorOrganization) {
                            mod = ((RealtorOrganization) organization).getMod();
                        }

                    }
                }
            }
        }
        aoi = bfa.addApartmentOrderItem();
        aoi.setRealtorApartment(apt);
        bfa.setCustomer(account.getEmployee());

        mod.addRealtorOrder(bfa);

        BuyFurnishedApartmentWorkRequest orderFurnishedAptWR = new BuyFurnishedApartmentWorkRequest();
        orderFurnishedAptWR.setOrderFurnishedApt(bfa);
        orderFurnishedAptWR.setStatus("Placed");
        orderFurnishedAptWR.setSender(account);
        account.getWorkQueue().getWorkRequestList().add(orderFurnishedAptWR);

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof RealtorOrganization) {
                organization.getWorkQueue().getWorkRequestList().add(orderFurnishedAptWR);
            }
        }

        JOptionPane.showMessageDialog(null, "Order Placed Successfully", "Information!!", JOptionPane.WARNING_MESSAGE);


    }//GEN-LAST:event_placeOrderJButtonActionPerformed


