    public void populateCustomerTable() {
        DefaultTableModel model = (DefaultTableModel) customisedOrderJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest wr : account.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof SelfDesignedWorkRequest) {
                Object[] row = new Object[4];
                SelfDesignedWorkRequest sdwr = (SelfDesignedWorkRequest) wr;
                row[0] = sdwr;
                row[1] = sdwr.getRealtorSender() == null ? null : sdwr.getRealtorSender().getEmployee().getName();
                row[2] = sdwr.getRequestDate();
                row[3] = sdwr.getStatus();
                model.addRow(row);
            }

        }
    }

    public void populateRealtorOrdersTable() {
        DefaultTableModel model = (DefaultTableModel) realtorOrdersJTable.getModel();

        model.setRowCount(0);
        {
            for (WorkRequest wr : account.getWorkQueue().getWorkRequestList()) {
                if (wr instanceof BuyFurnishedApartmentWorkRequest) {
                    Object[] row = new Object[4];
                    BuyFurnishedApartmentWorkRequest bfa = (BuyFurnishedApartmentWorkRequest) wr;
                    row[0] = bfa;
                    row[1] = bfa.getSender().getEmployee().getName();
                    row[2] = bfa.getRequestDate();
                    row[3] = bfa.getStatus();
                    model.addRow(row);

                }
            }
        }
    }

