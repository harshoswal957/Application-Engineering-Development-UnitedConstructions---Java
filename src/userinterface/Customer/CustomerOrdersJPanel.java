    private void checkOrderStatusJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOrderStatusJButtonActionPerformed
        //  progress bar used to check the order status
        int selectedRow = customisedOrderJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            SelfDesignedWorkRequest sdwr = (SelfDesignedWorkRequest) customisedOrderJTable.getValueAt(selectedRow, 0);
            if (sdwr.getStatus().equalsIgnoreCase("Placed")) {
                ordersStatusjProgressBar.setValue(15); //Set progress of order status
            } else if (sdwr.getStatus().equalsIgnoreCase("Order Final")) {
                ordersStatusjProgressBar.setValue(30);
            } else if (sdwr.getStatus().equalsIgnoreCase("Design Order")) {
                ordersStatusjProgressBar.setValue(50);
            } else if (sdwr.getStatus().equalsIgnoreCase("Pending with Design")) {
                ordersStatusjProgressBar.setValue(65);
            } else if (sdwr.getStatus().equalsIgnoreCase("Design Complete")) {
                ordersStatusjProgressBar.setValue(70);
            } else if (sdwr.getStatus().equalsIgnoreCase("Manufacture Order")) {
                ordersStatusjProgressBar.setValue(80);
                } else if (sdwr.getStatus().equalsIgnoreCase("Pending")) {
                ordersStatusjProgressBar.setValue(90);
                } else if (sdwr.getStatus().equalsIgnoreCase("Constructed")) {
                ordersStatusjProgressBar.setValue(90);
            } else if (sdwr.getStatus().equalsIgnoreCase("Apartment Ready")) {
                ordersStatusjProgressBar.setValue(100);
            }

        }
    }//GEN-LAST:event_checkOrderStatusJButtonActionPerformed


