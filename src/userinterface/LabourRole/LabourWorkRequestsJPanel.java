    private void apartmentFurnishedJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apartmentFurnishedJButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = designerWorkRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table first ", "Warning", JOptionPane.WARNING_MESSAGE);

            return;
        }

        SelfDesignedWorkRequest request = (SelfDesignedWorkRequest) designerWorkRequestJTable.getValueAt(selectedRow, 0);
        request.setStatus("Constructed");
        request.setLabourerSender(userAccount);
        request.setRealtorReceiver(null);
        userAccount.getWorkQueue().getWorkRequestList().add(request);
      
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise instanceof CustomerSupportEnterprise) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof RealtorOrganization) {
                            
                            organization.getWorkQueue().getWorkRequestList().add(request);

                        }
                    }

                }
            }
        }
        populateDesignerWorkRequestTable();

        JOptionPane.showMessageDialog(null, "Apartment ready for move-in", "Info", JOptionPane.INFORMATION_MESSAGE);


    }//GEN-LAST:event_apartmentFurnishedJButtonActionPerformed

