    public void populateDesignerWorkRequestTable() {
        DefaultTableModel model = (DefaultTableModel) designerWorkRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof SelfDesignedWorkRequest) {
                Object[] row = new Object[5];
                SelfDesignedWorkRequest sdwr = (SelfDesignedWorkRequest) wr;
                row[0] = sdwr;
                row[1] = sdwr.getDesignerSender() == null ? null : sdwr.getDesignerSender().getEmployee().getName();
                row[2] = sdwr.getLabourerReceiver() == null ? null : sdwr.getLabourerReceiver().getEmployee().getName();
                row[3] = sdwr.getRequestDate();
                row[4] = sdwr.getStatus();
                model.addRow(row);
            }

        }
    }

