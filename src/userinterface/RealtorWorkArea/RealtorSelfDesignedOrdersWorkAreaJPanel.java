    public void populateCustomerWorkRequestTable() {
        DefaultTableModel model = (DefaultTableModel) customerWorkRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest wr : realtorOrganization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof SelfDesignedWorkRequest) {
                Object[] row = new Object[5];
                SelfDesignedWorkRequest sdwr = (SelfDesignedWorkRequest) wr;
                row[0] = sdwr;
                row[1] = sdwr.getCustomerSender() == null ? null : sdwr.getCustomerSender().getEmployee().getName();
                row[2] = sdwr.getRealtorReceiver() == null ? null : sdwr.getRealtorReceiver().getEmployee().getName();
                row[3] = sdwr.getRequestDate();
                row[4] = sdwr.getStatus();
                model.addRow(row);
            }
        }

    }

   

    

